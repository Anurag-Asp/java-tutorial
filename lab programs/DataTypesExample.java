public class DataTypesExample {
    public static void main(String[] args) {
        // Example of Integer datatype
        int age = 25;
        System.out.println("Integer Example: Age = " + age);

        // Example of Double datatype
        double salary = 50000.75;
        System.out.println("Double Example: Salary = " + salary);

        // Example of Boolean datatype
        boolean isJavaFun = true;
        System.out.println("Boolean Example: Is Java Fun? = " + isJavaFun);

        // Example of String datatype
        String name = "John Doe";
        System.out.println("String Example: Name = " + name);
    }
}